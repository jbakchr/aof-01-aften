# "range" funktionen - med slutværdi
for i in range(5):
    print(i)


# "range" funktionen - med start- og slutværdi
for i in range(5, 11):
    print(i)


# range funktionen - med start- og slutværdi og trin
for i in range(5, 11, 2):
    print(i)


# "range" funktionen - med start- og slutværdi og negativt trin
for i in range(10, 0, -1):
    print(i)
